def _load_ucr_data(dataroot, dataset):
    train = pd.read_csv(os.path.join(dataroot, dataset, dataset + '_TRAIN.tsv'),
                        sep='\t', header=None)
    test = pd.read_csv(os.path.join(dataroot, dataset, dataset + '_TEST.tsv'),
                       sep='\t', header=None)

    train_x, train_y = train.iloc[:, 1:], train.iloc[:, 0]
    test_x, test_y = test.iloc[:, 1:], test.iloc[:, 0]

    col_mean = train_x.mean(axis=0).fillna(1e-6)
    train_x = train_x.fillna(col_mean)
    test_x = test_x.fillna(col_mean)

    train_x = normalize_per_series(train_x.to_numpy(np.float32))
    test_x = normalize_per_series(test_x.to_numpy(np.float32))

    train_x = pd.DataFrame(train_x)
    test_x = pd.DataFrame(test_x)

    all_y = pd.concat([train_y, test_y]).astype('category')
    class_names = all_y.cat.categories
    labels = all_y.cat.codes.to_numpy(np.int8)

    data = np.concatenate([train_x, test_x], axis=0)

    sample_ids, values = [], []
    for i in range(data.shape[0]):
        sample_ids.extend([i] * data.shape[1])
        values.extend([[v] for v in data[i]])

    feature_df = pd.DataFrame(values, index=sample_ids)
    labels_df = pd.DataFrame(labels, dtype=np.int8)

    return feature_df, labels_df, class_names


