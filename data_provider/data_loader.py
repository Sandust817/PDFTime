        labels_df = pd.DataFrame(labels.cat.codes, dtype=np.int8)

        lengths = df.applymap(len).values
        if np.any(lengths != lengths[:, [0]]):
            df = df.applymap(subsample)

        lengths = df.applymap(len).values
        self.max_seq_len = int(np.max(lengths[:, 0]))

        df = pd.concat(
            (
                pd.DataFrame({c: df.loc[i, c] for c in df.columns})
                .reset_index(drop=True)
                .set_index(pd.Series(lengths[i, 0] * [i]))
                for i in range(df.shape[0])
            ),
            axis=0
        )

        df = df.groupby(df.index).transform(interpolate_missing)
        return df, labels_df

